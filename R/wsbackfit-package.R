#' @title Weighted Smooth Backfitting for Structured Models.
#' @description
#' Nonparametric statistical methods for estimating generalized additive and partially linear models as well as generalized varying coefficient models. Specifically, the package provides estimation procedures for a large
#' class of regression models common in applied statistics. The regression models belong to the class of the so-called generalized structured models. They include for example
#' generalized varying coefficient and generalized additive models, both optionally partial linear. The estimation procedure is based on smoothed backfitting which to our knowledge
#' is the statistically most efficient existing procedure for this model class. Additional weights allow for both, the inclusion of different link functions and the efficient estimation under
#' heteroscedasticity. Also, a cross validation bandwidth selector is provided.
